#This program takes a user input for five years of wins and returns an average.

win1= int(input("Enter wins for year 1:"))
win2= int(input("Enter wins for year 2:"))
win3= int(input("Enter wins for year 3:"))
win4= int(input("Enter wins for year 4:"))
win5= int(input("Enter wins for year 5:"))

averageWins = (win1 + win2 + win3 + win4 + win5)/5

print ("The average of the five years is: ", averageWins)
